    private void selectItemButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selectItemButtonActionPerformed
        ArrayList<SelectorDeItems.ObjectPlaceholder> objectList = new ArrayList<>();
        Map<String, Recipe> recipes;

        try {
            recipes = JSONUtil.getRecipeMap();
        } catch (IOException e) {
            throw new RuntimeException(e);
        }

        for (Map.Entry<String, Recipe> entry : recipes.entrySet()) {
            String key = entry.getKey();
            Recipe value = entry.getValue();
            objectList.add(new SelectorDeItems.ObjectPlaceholder(key, value.getImageUrl()));
        }

        SelectorDeItems gui = new SelectorDeItems(objectList);
        gui.setVisible(false);
        
}//GEN-LAST:event_selectItemButtonActionPerformed

    private void openFileButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_openFileButton1ActionPerformed
        boolean isFile = false;

        JFileChooser chooser = new JFileChooser();
        chooser.showOpenDialog(null);
        File f = chooser.getSelectedFile();

        if (f != null) {
            String filename = f.getAbsolutePath();
            filePath1.setText(filename);
            isFile = true;

            return;
        }

        isFile = false;

    }//GEN-LAST:event_openFileButton1ActionPerformed

